window.clearPrizeState = clearPrizeState;
window.STORAGE_KEYS = STORAGE_KEYS;
window.readStorageArray = readStorageArray;
window.addUserToHistory = addUserToHistory;
window.addSpinToHistory = addSpinToHistory;
