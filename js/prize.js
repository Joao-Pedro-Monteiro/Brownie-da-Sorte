const STORAGE_KEYS = {
  listIndex: "brownie.listIndex",
  list: "brownie.list",
  position: "brownie.position",
  username: "brownie.username"
};

const PRIZE_MAP = {
  C2L3: {
    code: "C2L3",
    label: "Compre 2 Leve 3",
    angles: [45, 135, 225, 315]
  },
  BROWNIE_GRATIS: {
    code: "BROWNIE_GRATIS",
    label: "1 Brownie Gratis",
    angles: [0, 180, 270]
  },
  "100_REAIS": {
    code: "100_REAIS",
    label: "R$100,00",
    angles: [90]
  }
};

let list = [];
let listIndex = null;
let position = 0;

function logPrizeState(message, extra = {}) {
  console.log(`[Prize] ${message}`, {
    listIndex,
    position,
    listLength: list.length,
    ...extra
  });
}

function savePrizeState() {
  localStorage.setItem(STORAGE_KEYS.listIndex, String(listIndex));
  localStorage.setItem(STORAGE_KEYS.list, JSON.stringify(list));
  localStorage.setItem(STORAGE_KEYS.position, String(position));
  logPrizeState("Estado salvo no localStorage");
}

function clearPrizeState() {
  localStorage.removeItem(STORAGE_KEYS.listIndex);
  localStorage.removeItem(STORAGE_KEYS.list);
  localStorage.removeItem(STORAGE_KEYS.position);
  list = [];
  listIndex = null;
  position = 0;
  logPrizeState("Estado limpo");
}

function setList(forceNew = false) {
  if (!forceNew && Array.isArray(list) && list.length > 0) {
    logPrizeState("Mantendo lista atual");
    return list;
  }

  listIndex = Math.floor(Math.random() * catalogo.length);
  list = [...catalogo[listIndex]];
  position = 0;
  savePrizeState();
  logPrizeState("Nova lista sorteada", { sampledListIndex: listIndex });
  return list;
}

function restorePrizeState() {
  const savedList = localStorage.getItem(STORAGE_KEYS.list);
  const savedPosition = Number.parseInt(localStorage.getItem(STORAGE_KEYS.position), 10);
  const savedListIndex = Number.parseInt(localStorage.getItem(STORAGE_KEYS.listIndex), 10);

  if (savedList) {
    try {
      const parsedList = JSON.parse(savedList);
      if (Array.isArray(parsedList) && parsedList.length > 0) {
        list = parsedList;
        listIndex = Number.isInteger(savedListIndex) ? savedListIndex : null;
        position = Number.isInteger(savedPosition) && savedPosition >= 0 ? savedPosition : 0;

        if (position >= list.length) {
          logPrizeState("Posicao recuperada era invalida, resetando para 0", {
            recoveredPosition: position
          });
          position = 0;
          savePrizeState();
        }

        logPrizeState("Estado restaurado do localStorage");
        return;
      }
    } catch (error) {
      console.error("[Prize] Falha ao recuperar lista salva.", error);
    }
  }

  logPrizeState("Nenhum estado valido encontrado, sorteando nova lista");
  setList(true);
}

function getCurrentPrizeEntry() {
  if (!Array.isArray(list) || list.length === 0) {
    restorePrizeState();
  }

  if (!Array.isArray(list) || list.length === 0) {
    setList(true);
  }

  if (!Number.isInteger(position) || position < 0 || position >= list.length) {
    logPrizeState("Posicao atual invalida, resetando", { invalidPosition: position });
    position = 0;
    savePrizeState();
  }

  const prizeCode = list[position];
  const config = PRIZE_MAP[prizeCode];

  if (!config) {
    console.error("[Prize] Premio desconhecido encontrado na lista.", {
      prizeCode,
      position,
      listIndex
    });
    throw new Error(`Premio desconhecido: ${prizeCode}`);
  }

  const angleIndex = Math.floor(Math.random() * config.angles.length);
  const angle = config.angles[angleIndex];
  const result = {
    code: config.code,
    label: config.label,
    angle,
    position,
    listIndex
  };

  position += 1;

  if (position >= list.length) {
    logPrizeState("Fim da tabela atingido, nova lista sera preparada apos este sorteio", {
      deliveredPrize: result
    });
    setList(true);
  } else {
    savePrizeState();
  }

  logPrizeState("Premio entregue", result);
  return result;
}

function controller() {
  restorePrizeState();
  logPrizeState("Controlador inicializado");
  return {
    listIndex,
    position,
    listLength: list.length
  };
}

window.controller = controller;
window.getCurrentPrizeEntry = getCurrentPrizeEntry;
window.setList = setList;
window.restorePrizeState = restorePrizeState;
window.clearPrizeState = clearPrizeState;
window.STORAGE_KEYS = STORAGE_KEYS;
